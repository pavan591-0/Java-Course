package arithmetic;

public class Arithmetics {

	public static void main(String[] args) {
		 
		int a = 14, x;
		short b = 128;
		
		x = a + b;
		
		System.out.println(x); // 142
		
		float y = 14 + 14.5f;
		System.out.println(y); // 28.5
		
		float z = 14.3f % 3.2f;
		System.out.println(z); // 1.5	

	}

}
